export const getSubcategoryValueToTextMap = () => {
  const dict: any = {};
  Object.keys(SUBCATEGORY_OPTIONS).forEach(function (key) {
    // @ts-ignore
    const obj = SUBCATEGORY_OPTIONS[key];
    dict[obj.category] = obj.title;
    const subcategories = obj.subcategories;
    subcategories.forEach(function (value: any) {
      dict[value.value] = value.title;
    });
  });
  return dict;
};

